package Bean;

public class Style {
	int style_id;
	String style_name,style_description,style_img;
	public int getStyle_id() {
		return style_id;
	}
	public void setStyle_id(int style_id) {
		this.style_id = style_id;
	}
	public String getStyle_name() {
		return style_name;
	}
	public void setStyle_name(String style_name) {
		this.style_name = style_name;
	}
	public String getStyle_description() {
		return style_description;
	}
	public void setStyle_description(String style_description) {
		this.style_description = style_description;
	}
	public String getStyle_img() {
		return style_img;
	}
	public void setStyle_img(String style_img) {
		this.style_img = style_img;
	}
}
